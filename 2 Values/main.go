package main

import "fmt"

func main() {
	// STRING
	fmt.Println("ini folder ke 2 bro")

	// INTEGER
	fmt.Println(1 + 3)

	// FLOAT
	fmt.Println(0.3 * 5)

	// BOOLEAN
	fmt.Println()
	fmt.Println(true && false)
	fmt.Println(true || false)

	// NOT GATE
	fmt.Println("Not itu lawan/bukan")
	fmt.Println(!true)
	fmt.Println(!false)
}
